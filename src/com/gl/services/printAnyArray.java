package com.gl.services;
public class printAnyArray {
    public static void printDoubleArray(double []arr){
        for (int j = 0; j<arr.length; j++) {
            System.out.print(arr[j] + " ");
        }
        System.out.println();
    }
    //Created the below function to print and debug the input from user for stock prices change
    public static void printBooleanArray(boolean []arr){
        for (int j = 0; j<arr.length; j++) {
            System.out.print(arr[j] + " ");
        }
        System.out.println();
    }
}
